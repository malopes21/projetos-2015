    private void btnTransActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTransActionPerformed
        // TODO add your handling code here:
        Geometria geomBuscada = getGeometriaById(txtGeometria.getText());
        if(geomBuscada == null) {
            JOptionPane.showMessageDialog(this, "Geometria inexistente!");
        } else {
            String transformacao = comboTrans.getSelectedItem().toString();
            if("ROTAÇÃO".equals(transformacao)) {
                float ang = Float.parseFloat(txtDx.getText());
                for(int i=0; i<geomBuscada.getPontos().size(); i++) {
                    geomBuscada.getPontos().set(i, Transformacoes.rotacao(geomBuscada.getPontos().get(i), ang));
                }
            } else if("TRANSLAÇÃO".equals(transformacao)) {
                float dx = Float.parseFloat(txtDx.getText());
                float dy = Float.parseFloat(txtDy.getText());
                for(int i=0; i<geomBuscada.getPontos().size(); i++) {
                    geomBuscada.getPontos().set(i, Transformacoes.translacao(geomBuscada.getPontos().get(i), dx, dy));
                }
            } else if("ESCALA".equals(transformacao)) {
                float dx = Float.parseFloat(txtDx.getText());
                float dy = Float.parseFloat(txtDy.getText());
                for(int i=0; i<geomBuscada.getPontos().size(); i++) {
                    geomBuscada.getPontos().set(i, Transformacoes.escala(geomBuscada.getPontos().get(i), dx, dy));
                }
            }
            jPanel2.repaint();
        }
    }//GEN-LAST:event_btnTransActionPerformed

    private void btnTrans1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTrans1ActionPerformed
        // TODO add your handling code here:
        PainelDesenho pDes = (PainelDesenho) jPanel2;
        float zoom = Float.parseFloat(txtZoom.getText());
        pDes.setRealHeight(pDes.getRealHeight()/zoom);
        pDes.setRealWidth(pDes.getRealWidth()/zoom);
        
        float dxCenter = Float.parseFloat(txtDxCenter.getText());
        float dyCenter = Float.parseFloat(txtDyCenter.getText());
        pDes.setCenterX(pDes.getCenterX() + dxCenter);
        pDes.setCenterY(pDes.getCenterY() + dyCenter);
        
        pDes.repaint();
    }//GEN-LAST:event_btnTrans1ActionPerformed

