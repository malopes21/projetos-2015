    private void addGeometria(Geometria geometria) {
        Geometria geomBuscada = getGeometriaById(geometria.getId());
        if(geomBuscada != null) {
            JOptionPane.showMessageDialog(this, "Id de geometria já existente!");
        } else {
            ((PainelDesenho) jPanel2).getGeometrias().add(geometria);
        }
        
        //JOptionPane.showMessageDialog(this, "Adicionada!");
    }

